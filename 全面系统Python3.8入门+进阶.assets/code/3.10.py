print('hello'*2)
print('hello'[4])
print('hello'[0])
print("hello world"[6])
print("hello world"[-5])

# 截取字符串 多数一位
print("hello world"[0:5])  # hello
print("hello world"[0:-1])  # hello worl

# 截取world 冒号前面默认0，后面默认最后一位位置
print("hello world"[6:11])  # world
print("hello world"[6:])  # world
print("hello world"[-5:])  # world
